
function [yy1, yy2] = hermitejiho(a,b,y,w,xx)

    L = { ...
        @(x) (b-x).^2.*(a-b+2*b.*x-2*a*b)./(a-b)^3, ....
        @(x) (a-x).^2.*(b-a+2*a.*x-2*a*b)./(a-b)^3, ....
        @(x) (b-x).^2.*(x-a)/(a-b)^2, ....
        @(x) (a-x).^2.*(x-b)/(a-b)^2};
    
    yy1 = y(1)*L{1}(xx) + y(2)*L{2}(xx) + w(1)*L{3}(xx) + w(2)*L{4}(xx);
    
    L2 = {...
    @(x) (b-x).^2 .* (3*a-b-2*x) ./(a-b).^3 ,....
    @(x) (a-x).^2 .* (a-3*b+2*x)./(a-b).^3, ....
    @(x) (b-x).^2 .* (x-a) ./ (a-b).^2 , ...
    @(x) (a-x).^2 .* (x-b) ./ (a-b).^2 };

    yy2 = y(1)*L2{1}(xx) + w(1)*L2{3}(xx) + ...
     y(2)*L2{2}(xx) + w(2)*L2{4}(xx);
    
    plot(xx,yy1, 'bo', xx,yy2,'ro');

end